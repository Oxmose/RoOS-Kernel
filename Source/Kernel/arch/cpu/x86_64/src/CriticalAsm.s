%include "config.inc"

;-------------------------------------------------------------------------------
; ARCH
;-------------------------------------------------------------------------------
[bits 64]

;-------------------------------------------------------------------------------
; DEFINES
;-------------------------------------------------------------------------------
; None

;-------------------------------------------------------------------------------
; MACRO DEFINE
;-------------------------------------------------------------------------------
; None

;-------------------------------------------------------------------------------
; EXTERN DATA
;-------------------------------------------------------------------------------
; None

;-------------------------------------------------------------------------------
; EXTERN FUNCTIONS
;-------------------------------------------------------------------------------
; None

;-------------------------------------------------------------------------------
; EXPORTED FUNCTIONS
;-------------------------------------------------------------------------------
global SpinlockAcquire
global SpinlockRelease
global AtomicIncrement32
global AtomicDecrement32
global CPUMemoryFenceRelease
global CPUMemoryFenceAcquire

;-------------------------------------------------------------------------------
; EXPORTED DATA
;-------------------------------------------------------------------------------
; None

;-------------------------------------------------------------------------------
; CODE
;-------------------------------------------------------------------------------
section .text
align 4

;-------------------------------------------------------------------------------
; Lock Spinlock
;
; Param:
;     Input: rdi: Address of the lock

SpinlockAcquire:
  lock bts dword [rdi], 0
  jc   SpinlockAcquire
  ret

__pauseSpinlockPause:
  pause
  test  dword [rdi], 1
  jnz   __pauseSpinlockPause
  jmp   SpinlockAcquire

;-------------------------------------------------------------------------------
; Unlock Spinlock
;
; Param:
;     Input: rdi: Address of the lock

SpinlockRelease:
  mov  dword [rdi], 0
  ret

;-------------------------------------------------------------------------------
; 32 Bits atomic increment
;
; Param:
;     Input: rdi: u32_atomic_t value to increment
AtomicIncrement32:
  mov  eax, 1
  lock xadd dword [rdi], eax
  ret

;-------------------------------------------------------------------------------
; 32 Bits atomic decrement
;
; Param:
;     Input: rdi: u32_atomic_t value to decrement
AtomicDecrement32:
  mov  eax, -1
  lock xadd dword [rdi], eax
  ret

;-------------------------------------------------------------------------------
; CPU Memory acquire fence
;
; Param:
;     None
CPUMemoryFenceAcquire:
  mfence
  ret

;-------------------------------------------------------------------------------
; CPU Memory release fence
;
; Param:
;     None
CPUMemoryFenceRelease:
  mfence
  ret

;-------------------------------------------------------------------------------
; DATA
;-------------------------------------------------------------------------------
; None

; EOF